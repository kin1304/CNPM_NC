const container_Masothue = document.getElementById('Masothue');
const letters_Masothue = document.querySelectorAll('.letterMasothue');

container_Masothue.addEventListener('mouseover', () => {
    letters_Masothue.forEach((letter, index) => {
        setTimeout(() => {
            letter.classList.add('jump');
        }, index * 50); // Độ trễ 50ms giữa các chữ cái
    });
});
container_Masothue.addEventListener('mouseout', () => {
    letters_Masothue.forEach((letter, index) => {
        setTimeout(() => {
            letter.classList.remove('jump');
        }, index * 50); // Độ trễ 50ms giữa các chữ cái
    });
});




