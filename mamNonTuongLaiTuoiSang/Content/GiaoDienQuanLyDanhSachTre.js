// Trẻ em 1
function playSound1() {
    var sound = document.getElementById("1convit");
    sound.currentTime = 0; // Đặt thời gian phát lại về 0
    sound.play().catch(error => {
        console.error("Không thể phát âm thanh: ", error);
    });
}
function stopSound1() {
    var sound = document.getElementById("1convit");
    sound.pause(); // Tạm dừng âm thanh
    sound.currentTime = 0; // Đặt lại thời gian phát về 0
}

